n = int(input("Введите число от 1 до 99: "))
if 1 <= n <= 99:
    print(n, end=" ")
    if (n == 1 or (n % 10) == 1) & (n != 11):
        print("копейка")
    if (2 <= n <= 4 or (2 <= (n % 10) <= 4)) & (n != 12) & (n != 13) & (n != 14):
        print("копейки")
    if 5 <= n <= 20 or (5 <= (n % 10) <= 9) or ((n % 10) == 0):
        print("копеек")
else:
    print("Введено число не из заданного диапазона, "
          "введите число от 1 до 99")
